    class Math_Operators
    {
        public static int Objects(int x, int y)
        {
            
            return x * y;
        }

        public static Decimal Objects(Decimal a, Decimal b)

        {
            return a - b;
        }

        public static string Objects(string c, string d)
        {

            return c + d;
        }

       
